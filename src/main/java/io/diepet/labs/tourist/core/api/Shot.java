package io.diepet.labs.tourist.core.api;

/**
 * The Shot interface.
 */
public interface Shot {

	/**
	 * Gets the picture of the shot.
	 *
	 * @return the picture
	 */
	public String getPicture();

	/**
	 * Gets the timestamp of the shot.
	 *
	 * @return the timestamp
	 */
	public long getTimestamp();

}
